#!/usr/bin/env node
// ── ERP Throughput-Accounting benchmark generator ──
//
// Generates a set of synthetic "ERP period extracts" (companies, work
// centers, products, routings, market demand, operating expenses) and a set
// of Theory-of-Constraints throughput-accounting questions with computed
// ground-truth answers (constraint identification, throughput-per-unit,
// T/CU ranking, optimal product mix under the constraint, total throughput,
// net profit, and a "trap" question contrasting throughput accounting with
// traditional margin-per-unit reasoning).
//
// Deterministic (seeded PRNG) so re-running produces identical output.
//
// Usage:
//   npx tsx eval/scripts/generate-erp-benchmark.ts [--companies 8] [--seed 42]
//
// Writes:
//   eval/erp/companies.json  — raw ERP data (what the agent's erp-report
//                              tool exposes; ids + names + numbers only, no
//                              answers).
//   eval/erp/tasks.json      — questions + ground-truth expected answers.
//
// Unlike the GAIA dataset, this data is entirely synthetic and generated by
// this script, so it is safe to commit (no third-party license restriction).

import { writeFileSync, mkdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, '..', '..');

// ── Seeded PRNG (mulberry32) for reproducibility ──
function mulberry32(seed: number) {
  let a = seed;
  return function () {
    a |= 0;
    a = (a + 0x6d2b79f5) | 0;
    let t = Math.imul(a ^ (a >>> 15), 1 | a);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

type RNG = () => number;

function randInt(rng: RNG, min: number, max: number, step = 1): number {
  const steps = Math.floor((max - min) / step);
  return min + step * Math.floor(rng() * (steps + 1));
}

function randFloat(rng: RNG, min: number, max: number): number {
  return min + rng() * (max - min);
}

function choice<T>(rng: RNG, arr: T[]): T {
  return arr[Math.floor(rng() * arr.length)];
}

// ── Domain types ──
interface WorkCenter {
  id: string;
  name: string;
  availableMinutes: number;
}

interface Product {
  id: string;
  name: string;
  price: number;
  totallyVariableCost: number;
  throughputPerUnit: number; // price - totallyVariableCost
  demandUnits: number; // max market demand this period
  routing: Record<string, number>; // workCenterId -> minutes required per unit
}

interface Company {
  id: string;
  name: string;
  period: string;
  workCenters: WorkCenter[];
  products: Product[];
  operatingExpenses: number;
}

interface Analytics {
  constraintWcId: string;
  tcuRanking: string[]; // product ids, highest T/CU first
  throughputRanking: string[]; // product ids, highest throughput/unit first
  mix: Record<string, number>; // productId -> units produced in optimal mix
  cutoffProductId: string; // the product partially produced (the binding one)
  totalThroughput: number; // $ from the optimal mix
  netProfit: number; // totalThroughput - operatingExpenses
  marginMismatch: boolean; // true if throughputRanking[0] !== tcuRanking[0]
}

const WC_NAMES = ['Cutting', 'Assembly', 'Finishing', 'Packing'];
const PRODUCT_LETTERS = ['A', 'B', 'C', 'D', 'E'];
const COMPANY_NAMES = [
  'Acme Manufacturing',
  'Blue Ridge Industries',
  'Summit Fabrication',
  'Coastal Components Co.',
  'Ironvale Works',
  'Redwood Precision Parts',
  'Harbor Point Machining',
  'Prairie Steel Products',
  'Silverline Assembly',
  'Northgate Industrial',
];

function buildCompany(rng: RNG, idx: number): { company: Company; analytics: Analytics } {
  const numWc = 4;
  const numProducts = randInt(rng, 4, 5);

  const wcIds = WC_NAMES.slice(0, numWc).map((_, i) => `WC${i + 1}`);
  const workCenters: WorkCenter[] = wcIds.map((id, i) => ({
    id,
    name: WC_NAMES[i],
    availableMinutes: 0, // filled in below
  }));

  const products: Product[] = [];
  for (let p = 0; p < numProducts; p++) {
    const price = randInt(rng, 40, 160, 5);
    const marginPct = randFloat(rng, 0.25, 0.6);
    let tvc = Math.round((price * (1 - marginPct)) / 5) * 5;
    if (tvc >= price) tvc = price - 5;
    if (tvc < 5) tvc = 5;
    const throughputPerUnit = price - tvc;
    const demandUnits = randInt(rng, 20, 100, 10);

    const routing: Record<string, number> = {};
    for (const wc of wcIds) routing[wc] = randInt(rng, 2, 16);

    products.push({
      id: `P${p + 1}`,
      name: `Product ${PRODUCT_LETTERS[p]}`,
      price,
      totallyVariableCost: tvc,
      throughputPerUnit,
      demandUnits,
      routing,
    });
  }

  // Required minutes per work center if ALL market demand were satisfied.
  const requiredMinutes: Record<string, number> = {};
  for (const wc of wcIds) {
    requiredMinutes[wc] = products.reduce((sum, p) => sum + p.demandUnits * p.routing[wc], 0);
  }

  // Pick which work center is the constraint (binding resource).
  const constraintWcId = choice(rng, wcIds);

  // T/CU ranking at the constraint (throughput per unit / minutes required at
  // the constraint per unit) — highest first. This determines production
  // priority under Theory of Constraints.
  const byTcu = [...products].sort(
    (a, b) => b.throughputPerUnit / b.routing[constraintWcId] - a.throughputPerUnit / a.routing[constraintWcId],
  );

  // If the naive "highest $ throughput per unit" product happens to also be
  // the highest T/CU product, nudge its constraint-minutes up so the two
  // rankings genuinely diverge — this is the interesting case throughput
  // accounting is designed to catch (a traditional margin-per-unit view
  // recommends the wrong product to prioritize under the constraint).
  const byThroughputUnit = () => [...products].sort((a, b) => b.throughputPerUnit - a.throughputPerUnit);
  let throughputRanking = byThroughputUnit();
  let tcuRanking = byTcu;
  for (let attempt = 0; attempt < 5 && throughputRanking[0].id === tcuRanking[0].id; attempt++) {
    const top = throughputRanking[0];
    top.routing[constraintWcId] += randInt(rng, 8, 20);
    tcuRanking = [...products].sort(
      (a, b) => b.throughputPerUnit / b.routing[constraintWcId] - a.throughputPerUnit / a.routing[constraintWcId],
    );
    throughputRanking = byThroughputUnit();
  }

  // Re-derive required minutes at the constraint after any routing nudge.
  const constraintRequired = products.reduce((s, p) => s + p.demandUnits * p.routing[constraintWcId], 0);

  // Choose a cutoff rank k (1 .. numProducts-2) so that at least one product
  // after the cutoff is zeroed out (a genuine, non-trivial constraint).
  const maxK = numProducts - 2;
  if (maxK < 1) throw new Error('not enough products for a non-trivial cutoff');
  const k = randInt(rng, 1, maxK);

  const priority = tcuRanking; // highest T/CU first
  let cumMinutes = 0;
  for (let i = 0; i < k; i++) cumMinutes += priority[i].demandUnits * priority[i].routing[constraintWcId];

  const cutoffProduct = priority[k];
  const cutoffMinutesPerUnit = cutoffProduct.routing[constraintWcId];
  // Partial units of the cutoff product: strictly between 0 and its demand.
  const maxPartial = cutoffProduct.demandUnits - 1;
  if (maxPartial < 1) throw new Error('cutoff product demand too small');
  const partialUnits = randInt(rng, 1, maxPartial);

  const constraintAvailableMinutes = cumMinutes + partialUnits * cutoffMinutesPerUnit;

  // Sanity: constraint's available minutes must be less than what's needed
  // to satisfy full demand for everything (otherwise it isn't a constraint).
  if (constraintAvailableMinutes >= constraintRequired) {
    throw new Error('constraint not binding — retry with a different seed');
  }

  // Non-constraint work centers get generous slack so they are never binding.
  for (const wc of workCenters) {
    if (wc.id === constraintWcId) {
      wc.availableMinutes = constraintAvailableMinutes;
    } else {
      const required = products.reduce((s, p) => s + p.demandUnits * p.routing[wc.id], 0);
      wc.availableMinutes = required + randInt(rng, 200, 800, 50);
    }
  }

  // Build the optimal mix: fully produce top-k, partially produce the
  // cutoff product, zero for everything else.
  const mix: Record<string, number> = {};
  for (const p of products) mix[p.id] = 0;
  for (let i = 0; i < k; i++) mix[priority[i].id] = priority[i].demandUnits;
  mix[cutoffProduct.id] = partialUnits;

  const totalThroughput = products.reduce((s, p) => s + mix[p.id] * p.throughputPerUnit, 0);
  const operatingExpenses = Math.round((totalThroughput * randFloat(rng, 0.4, 0.8)) / 10) * 10;
  const netProfit = totalThroughput - operatingExpenses;

  const company: Company = {
    id: `C${idx + 1}`,
    name: COMPANY_NAMES[idx % COMPANY_NAMES.length],
    period: 'Q3',
    workCenters,
    products,
    operatingExpenses,
  };

  const analytics: Analytics = {
    constraintWcId,
    tcuRanking: tcuRanking.map((p) => p.id),
    throughputRanking: throughputRanking.map((p) => p.id),
    mix,
    cutoffProductId: cutoffProduct.id,
    totalThroughput,
    netProfit,
    marginMismatch: throughputRanking[0].id !== tcuRanking[0].id,
  };

  return { company, analytics };
}

function generateCompanyWithRetries(rng: RNG, idx: number): { company: Company; analytics: Analytics } {
  let lastErr: unknown;
  for (let attempt = 0; attempt < 200; attempt++) {
    try {
      return buildCompany(rng, idx);
    } catch (err) {
      lastErr = err;
    }
  }
  throw new Error(`Failed to generate company ${idx}: ${String(lastErr)}`);
}

interface Task {
  task_id: string;
  company_id: string;
  level: 1 | 2 | 3;
  type: string;
  question: string;
  expected_answer: string;
  answer_type: 'number' | 'id' | 'yes_no';
}

function productName(company: Company, id: string): string {
  const p = company.products.find((x) => x.id === id);
  return p ? p.name : id;
}

function wcName(company: Company, id: string): string {
  const wc = company.workCenters.find((x) => x.id === id);
  return wc ? wc.name : id;
}

function buildTasks(company: Company, analytics: Analytics): Task[] {
  const tasks: Task[] = [];
  const idPrefix = company.id;

  // L1 — single-fact lookup/calc
  const sampleProduct = company.products[0];
  tasks.push({
    task_id: `${idPrefix}-T1`,
    company_id: company.id,
    level: 1,
    type: 'throughput_per_unit',
    question:
      `For ${company.name}, what is the per-unit throughput (selling price minus totally variable cost) ` +
      `of "${sampleProduct.name}", in dollars?`,
    expected_answer: String(sampleProduct.throughputPerUnit),
    answer_type: 'number',
  });

  tasks.push({
    task_id: `${idPrefix}-T2`,
    company_id: company.id,
    level: 1,
    type: 'constraint_identification',
    question:
      `For ${company.name} this period, which work center is the constraint (the bottleneck resource that ` +
      `does not have enough capacity to meet full market demand for every product)? Answer with just the ` +
      `work center's name.`,
    expected_answer: wcName(company, analytics.constraintWcId),
    answer_type: 'id',
  });

  // L2 — ranking / two-step reasoning
  tasks.push({
    task_id: `${idPrefix}-T3`,
    company_id: company.id,
    level: 2,
    type: 'tcu_ranking',
    question:
      `For ${company.name}, which product has the HIGHEST throughput per constraint-minute (T/CU) at the ` +
      `constraint work center? Answer with just the product's name.`,
    expected_answer: productName(company, analytics.tcuRanking[0]),
    answer_type: 'id',
  });

  tasks.push({
    task_id: `${idPrefix}-T4`,
    company_id: company.id,
    level: 2,
    type: 'throughput_per_unit_ranking',
    question:
      `For ${company.name}, which product has the HIGHEST per-unit throughput in absolute dollar terms ` +
      `(selling price minus totally variable cost), ignoring how much constraint capacity it uses? ` +
      `Answer with just the product's name.`,
    expected_answer: productName(company, analytics.throughputRanking[0]),
    answer_type: 'id',
  });

  tasks.push({
    task_id: `${idPrefix}-T5`,
    company_id: company.id,
    level: 2,
    type: 'optimal_mix_units',
    question:
      `For ${company.name}, in the optimal product mix (produce the highest-T/CU products first, up to ` +
      `their market demand, until the constraint's available capacity is used up), how many units of ` +
      `"${productName(company, analytics.cutoffProductId)}" should be produced?`,
    expected_answer: String(analytics.mix[analytics.cutoffProductId]),
    answer_type: 'number',
  });

  // L3 — multi-step, requires the full mix + aggregation
  tasks.push({
    task_id: `${idPrefix}-T6`,
    company_id: company.id,
    level: 3,
    type: 'total_throughput',
    question:
      `For ${company.name}, what is the total throughput (in dollars) generated by the optimal product mix ` +
      `this period, after allocating the constraint's available capacity by T/CU priority?`,
    expected_answer: String(analytics.totalThroughput),
    answer_type: 'number',
  });

  tasks.push({
    task_id: `${idPrefix}-T7`,
    company_id: company.id,
    level: 3,
    type: 'net_profit',
    question:
      `For ${company.name}, what is the net profit for the period (total throughput of the optimal mix ` +
      `minus operating expenses), in dollars?`,
    expected_answer: String(analytics.netProfit),
    answer_type: 'number',
  });

  tasks.push({
    task_id: `${idPrefix}-T8`,
    company_id: company.id,
    level: 3,
    type: 'throughput_accounting_trap',
    question:
      `A traditional cost accountant at ${company.name} recommends prioritizing production of whichever ` +
      `product has the highest throughput (margin) per unit in absolute dollar terms. Would that ` +
      `recommended product be the SAME product that throughput accounting recommends prioritizing first ` +
      `(the highest T/CU product at the constraint)? Answer Yes or No.`,
    expected_answer: analytics.marginMismatch ? 'No' : 'Yes',
    answer_type: 'yes_no',
  });

  return tasks;
}

function main() {
  const args = process.argv.slice(2);
  const opts: Record<string, string> = {};
  for (let i = 0; i < args.length; i++) {
    if (args[i].startsWith('--')) {
      const key = args[i].slice(2);
      const val = args[i + 1] && !args[i + 1].startsWith('--') ? args[++i] : 'true';
      opts[key] = val;
    }
  }
  const numCompanies = opts.companies ? parseInt(opts.companies, 10) : 8;
  const seed = opts.seed ? parseInt(opts.seed, 10) : 42;

  const rng = mulberry32(seed);
  const companies: Company[] = [];
  const tasks: Task[] = [];

  for (let i = 0; i < numCompanies; i++) {
    const { company, analytics } = generateCompanyWithRetries(rng, i);
    companies.push(company);
    tasks.push(...buildTasks(company, analytics));
  }

  const erpDir = join(REPO_ROOT, 'eval', 'erp');
  mkdirSync(erpDir, { recursive: true });
  writeFileSync(join(erpDir, 'companies.json'), JSON.stringify(companies, null, 2));
  writeFileSync(join(erpDir, 'tasks.json'), JSON.stringify(tasks, null, 2));

  console.log(`Generated ${companies.length} companies, ${tasks.length} tasks.`);
  console.log(`Wrote eval/erp/companies.json and eval/erp/tasks.json`);
}

main();
